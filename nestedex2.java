public class nestedex2 {
    public static void main(String args[]){
        for(int j=0;j<=2;j++){
        for(int i=0;i<=j;i++){
            System.out.print("*"+" ");
        }
        System.out.println();
    }
    for(int i=3;i>=1;i--){
        for(int j=1;j<=i;j++){
            System.out.print("*"+" ");
        }
        System.out.println();
    }
    }   
}
